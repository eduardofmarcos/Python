# ex007>>>Desenvolva um programa que leia duas notas de um alundo, e mostre sua media

n1 = float(input('Digite a nota 1: ')) #insira nota um
n2 = float(input('Digite a nota 2: ')) #insita nota dois

print('A primeira nota é: {:.2f}, a segunda nota é: {:.2f} e a media do aluno é: {:.2f}'.format(n1, n2, (n1 + n2) / 2))#printa resultado
